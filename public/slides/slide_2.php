    <h1 class="Title has-text-link">Why use GIT?</h1>
    <ul class="presentation">
        <li>Software projects generally have multiple developers working at the same time.</li>
        <li>A version control system like Git is needed to ensure there are no code conflicts between the developers.</li>
        <li>A version control system allows developers (in case something goes terribly wrong) to go back to an older version of the code.</li> 
    </ul>
